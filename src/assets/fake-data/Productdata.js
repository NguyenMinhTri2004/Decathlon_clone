const getAllProducts = () => ProductArray

const getProductByType = (type) => ProductArray.filter(e => e.type === type)

const getProductBySlug = (slug) => ProductArray.find(e => e.slug === slug)

const getProductSimilar = (name) => ProductArray.filter(e => e.slug.includes(name))

const getProducts = (count) => {
    const max = ProductArray.length - count
    const min = 0
    const start = Math.floor(Math.random() * (max - min) + min)
    return ProductArray.slice(start, start + count)
}

const getCartItemsDetail = (cartItems) => {
     let res  = []
     if(cartItems.length > 0) {
         cartItems.forEach((e) =>{
            res.push({
                ...e,
                product : getProductBySlug(e.slug)
            })
         })
     }

     return res
}

